const int MX = 2005;
int n;
ll mem[MX][MX];
vii A;

ll dp(int l, int r) {
	if (l > r)
		return 0;
	ll& ret = mem[l][r];

	if (ret != -1)
		return ret;
	int idx = n - (r - l);
	db(l, r, idx, A[idx]);
	return ret = max(A[idx].fs * 1LL * abs(l - A[idx].sc) + dp(l + 1, r), A[idx].fs * 1LL * abs(r - A[idx].sc) + dp(l, r - 1));
}



